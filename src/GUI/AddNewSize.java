    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String tableName = "size";
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please add value", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySql.search("SELECT * FROM " + tableName + " WHERE `name`= '" + name + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This " + tableName + " already exsist !", "Duplicate value", JOptionPane.WARNING_MESSAGE);
                    jTextField1.grabFocus();
                } else {
                    MySql.iud("INSERT INTO " + tableName + " (`name`) VALUES ('" + name + "')");
                    JOptionPane.showMessageDialog(this, "New value added succesfully", "success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            loadResult();
        }
        jTextField1.grabFocus();

    }//GEN-LAST:event_jButton1ActionPerformed

