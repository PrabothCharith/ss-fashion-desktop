    public void loadResult() {
        try {
            ResultSet rs = MySql.search("SELECT * FROM size");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("size.id"));
                v.add(rs.getString("size.name"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

